跨域是指的浏览器跨域，由于同源策略的限制，无法共享不同域名的资源，同时也限制了DOM的读取。要强调的是，跨域的问题是存在于浏览器上，因为对于跨域的请求其实是可以发送，服务器也可以通过接受请求并正常返回结果，只是结果被浏览器截胡。

跨域是指从一个域名的网页去请求另一个域名的资源。比如从http://www.baidu.com/ 页面去请求 http://www.google.com 的资源。跨域的严格一点的定义是：只要 协议，域名，端口有任何一个的不同，就被当作是跨域。 
就是浏览器不允许当前页面的所在的源去请求另一个源的数据。源的定义:协议+二级域名+端口确定一个源。

